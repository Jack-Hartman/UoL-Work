CREATE PROCEDURE vehicleLocation(IN id int, IN inDate TEXT, IN checkTime Text)
BEGIN
    SELECT locations.latitude, locations.longitude, drivers.driverID, drivers.first_name, drivers.last_name, locations.timestamp FROM locations INNER JOIN routes ON locations.routeID = routes.routeID INNER JOIN drivers ON routes.driverID = drivers.driverID INNER JOIN shifts ON routes.shiftID = shifts.shiftID INNER JOIN vehicles ON routes.vehicleID = vehicles.vehicleID WHERE vehicles.vehicleID = id AND shifts.date = inDate AND locations.TIMESTAMP >= checkTime ORDER BY locations.TIMESTAMP LIMIT 1;
END//

CREATE PROCEDURE parcelsDelivered(IN id int, IN inDate TEXT)
BEGIN
    SELECT COUNT(deliveries.routeID) AS 'delivered' FROM deliveries INNER JOIN routes ON deliveries.routeID = routes.routeID INNER JOIN shifts ON routes.shiftID = shifts.shiftID WHERE routes.driverID = id AND shifts.date = inDate;
END//

